public class Variable01{
  public static void main(String[] args){
    // 変数を定義
    int num1;
    String str1;
    // 変数に値を代入
    num1 = 10;
    // Stringに文字を代入
    str1 = "Hello World!";
    System.out.println(num1);
    System.out.println(str1);

    // 再代入
    num1 = 20;
    System.out.println(num1);

    int num2 = 30;
    System.out.println(num2);
  }
}